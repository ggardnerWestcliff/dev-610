/*
--Exercise 5--

Write a complete Java program called MuchBetter that prints the following output:

A "quoted" String is
'much' better if you learn
the rules of "escape sequences."
Also, "" represents an empty String.
Don't forget: use \" instead of " !
'' is not the same as "

*/

public class MuchBetter {
    public static void main(String[] args) {
        System.out.println("A \"quoted\" String is\n'much' better if you learn\nthe rules of \"escape sequences.\"");
        System.out.println("Also, \"\" represents an empty String.\nDon't forget: use \\\" instead of \" !");
        System.out.println("'' is not the same as \"");
    }
}
